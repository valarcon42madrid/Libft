#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*aux;

	aux = lst;
	if (!lst)
		return (lst);
	while (aux->next != 0)
	{
		aux = aux->next;
	}
	return (aux);
}
